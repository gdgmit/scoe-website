      <h1>ABOUT ANNA UNIVERSITY – SIEMENS CENTRE OF EXCELLENCE</h1>
    </div>

    <div class="hideme" style="opacity: 1">
      <div id="mission">
        <p>The Hon’ble Chief Minister Late Dr. J. Jayalalithaa had made the announcement on 20.09.2016 to establish a Centre of Excellence and 5 linked Technical Skill Development Institutes (t-SDIs) in Tamilnadu through Public Private Partnership (PPP) in collaboration with Siemens and Designtech Limited at an 
          estimated cost of Rs.546.84 crores with state contribution of Rs 54.68 Crores from Tamilnadu Skill Development Corporation Limited.</p>
          <p>The Centre of Excellence (CoE) is established at MIT Campus of Anna University and the Technical Skill development Institutes(t-SDIs) at University College Of Engineering-Kancheepuram, University College Of Engineering-Villupuram, ThanthaiPeriyar Government 
            Institute of Technology-Vellore, Central Polytechnic College-Taramani and Murugappa Polytechnic College-Avadi.</p>
            <p>The main scope of the CoE and t-SDI’s will include:</p></div>
            <div id="mission2">
            <p>• Improvements in college labs infrastructure by adopting latest open
   technologies in engineering design, manufacturing, planning, 
   analysis and management to serve the needs of all industrial units 
   irrespective of use of the machinery and technologies.</p>
   <p>• Skill up gradation of faculty by training</p><p>• Update course curriculum to modern industrial practices.</p>
<p>• Promote research & Development and innovation for existing industries.</p>
<p>• Training of students to improve employability.</p>
<p>• Enhance the technical education institutions as modern and equipped with state of art technologies.</p>
<p>• Availability of trained workforce to attract industries to set up units in the state.</p>
<p></p>
<p></p>
</div>

